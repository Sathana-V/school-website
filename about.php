SVMHS Mathura Road was the only DPS till 1972. Motivated by a simple dream to educate, we started adding branches so as to take quality education to maximum number of children. R.K. Puram was our first branch. As on Ist September 2002, we were 95 in number.

The involvement of the Society in universal educational enhancement and its endeavour to build a democratic, pluralistic and secular nation has made all the difference. What has remained constant throughout these years is our zest and spirit to achieve and excel.</p> 
